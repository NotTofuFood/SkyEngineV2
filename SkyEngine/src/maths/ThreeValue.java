package maths;

public class ThreeValue {
	
	private double x;
	private double y;
	private double z;
	
	public ThreeValue(double f, double g, double h) {
		this.x = f;
		this.y = g;
		this.z = h;
	}

	public double getX() {
		return x;
	}
	public double getY() {
		return y;
	}
	public double getZ() {
		return z;
	}	
	
}
