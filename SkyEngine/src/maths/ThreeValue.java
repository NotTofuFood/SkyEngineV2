package maths;

public class ThreeValue {
	
	private int x;
	private int y;
	private int z;
	public ThreeValue(int x, int y, int z) {
		super();
		this.x = x;
		this.y = y;
		this.z = z;
	}
	public int getX() {
		return x;
	}
	public int getY() {
		return y;
	}
	public int getZ() {
		return z;
	}	
}
